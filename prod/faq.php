<h1>FAQ</h1>
</center>
<p>
<b>Q: Where can I get more information about Project Management ?</b><br>
A: I reccomend you go to <a href="http://www.pmi.org">pmi.org</a>. 
If you're serious, you should also consider joining your local PMP chapter. PMI.org can direct you to them.
<p>
<hr>
<p>
<b>Q: What course of study/resources to you reccomend for passing the PMP?</b><br>
A: I found the test more difficult than my physics courses at college. 
The content of the test is not that difficult, but the format and method of testing makes passing extremely challenging.
I reccomend a disciplined course of self study with color-coded flash cards and reading the PMBOK cover to cover at least twice.
I also strongly reccomend you take a PMP preperation class. 
I attended a PMP training course at <a href="http://projectmanagementacademy.net/select-location.php">Project Management Academy</a>. I don't think I would have passed without it.
<p>
<hr>
<p>
<b>Q: Will all of this be on the PMP exam?</b><br>
A: No. The PMP exam mostly requires that you memorize the formulas and know when to use them. 
However, for some people like myself it is better to understand something than to memorize it. 
As a PM you may find the information here more appropriate to real-world applications.
<p>
<hr>
<p>
<b>Q: How accurate is all of this information?</b><br>
A: That is complicated. Since the PMBOK does not include much this information you can consider it extracirricular. 
The PMBOK does not define all of the methologies so much as collect and collate them. 
Hence the name of the text book is PMBOK: Project Management Body of Knowledge. 
The information here reflects my understanding. I can back that up with my PMP and a decade of experience, but some content is clearly opinion. 
However, I have also checked my answers with other friends and colleagues in the industry PMs and statisticians so they are as complete as possible. 
I plan to continue refining and improving them over time.
