<h3>Estimate To Complete</h3>	
<p>
This is an entirely intuitive and easily understood formula directly from the PMBOK. 
It is calculated as ETC = EAC - AC or sometimes as ETC â€“ ACWP. AC and ACWP are the same thing. 
I have also seen non-PMI affiliated sources confuse it with TCPI conceptually. In this case stick with the PMBOK; this formula is incontrovertible. 
The formula is much older than even the 1950s Navy PM standards and it's self-evident as to why. 
<p>
ETC is the money that will be spent to complete the remainder of a project in progress. 
Estimate at completion (EAC) is the current updated budget for the entirety of the project. 
Actual Cost (AC) is the money you have already spent to date on the project. 
The difference between these numbers is your estimate for what the remainder of the project will cost.
<p>
Ex. <br>
If AC = 10,000 and EAC = 60,000<br>
Using ETC = EAC - AC<br>
ETC is 60,000 - 10,000 = 50,000<br>
<p>
As stated above, it's important to note that you can derive any of these figures from the other two.<br>
<p>
Ex. <br>
EAC = EAC + AC <br>
If AC = 10,000 and EAC = 60,000<br>
50,000 + 10,000 = 60,000<br>
<p>
Ex. <br>
AC = ETC - EAC <br>
If AC = 10,000 and EAC = 60,000<br>
60,000 - 50,000 = 10,000<br>
<p>
<h3>DEDUCTIONS:</h3>
Thsi is some of the easiest math in the PMBK and it may feel rudimentary but it is on the PMP exam. 
For the PMP exam you need to be able to compute each of the figures from the available information.
It will not always be in the form you are accustomed to. So learn each formula forwards and backwards, even the easy ones.
<p>
The PMBOK also refers to a bottom-up ETC and a top-down ETC. 
The bottom-up ETC is the sum of how much all the remaining estimated costs will be. There is no formula for this. 
It's an accounting of all remaining costs at a very granular WBS level. 
This formula produces a top down ETC. It is less accurate but it has utility.
<p>
<h3>CONCLUSION:</h3>
<p>
No theories here. This kind of math is beyond reproach and has been in use since commerce began. 
The only issue you will run into with ETC is that the accuracy of your estimates directly effects the accuracy of any information you derive from them: GIGO.
To quote the great George Fuechsel, "garbage in, garbage out." 
