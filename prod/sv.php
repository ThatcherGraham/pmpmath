<h3>Schedule Variance</h3>	
<p>
Schedule variance is very similar to cost variance. 
So some of the explanation here also appears in the CV proof as some the variables are the same and the same concepts are used. 
As stated under cost variance, when you are mid-project there are two units by which you can measure your progress: time and money.
Schedule variance measures progress and in terms of time. But here we measure time in dollars. 
<p>
It's counter-intuitive, but schedule variance does not measure progress in time-based units. 
We are measuring progress in dollars not manhours. 
The SV formula is SV = EV - PV. EV is earned value and PV is planned value.
It is also sometimes expressed as SV = BCWP - BCWS
<p>
Earned value is the estimated value of the completed work. That's a bit dicey though. 
On many projects the product or service is useless and unsaleable in an incomplete form. 
But that's not what they mean by the word "value." The formula for earned value is EV = percent complete x BAC. 
Sometimes this is given as BCWP - ACWP; Budgeted Cost of work performed minus Actual cost of work performed. 
You can see that here "value" is not the laypersons definition, here the meaning is more like planned cost. 
<p>
The formula for planned value is PV = Planned percent complete x BAC. Please note that the single difference between that and EV is the single word "planned." 
EV is your actual progress, PV is where you should be according to schedule. 
Like the CV formula the SV formula produces a measure of the difference between the plan and reality. 
If you have made less progress than was planned that formula will obviously produce a negative number. 
<p>
Ex. A<br>
EV = 25,000 and PV = 45,000<br>
So SV = 25,000 - 45,000 = -20,000
<p>
Ex. B<br>
EV = 45,000 and PV = 25,000<br>
So SV= 45,000 - 25,000 = 20,000
<p>
Ex. C<br>
EV = 25,000 and PV = 25,000<br>
So SV= 25,000 - 25,000 = 0<br>
<p>
Much like other simple project management formulas you can produce any of these figures from the other two. 
<p>
Ex. D<br>
EV = 25,000 and AC = 25,000<br>
So SV = EV - PV or 25,000 - 25,000 = 0<br>
and EV = PV + SV or 25,000 + 0 = 25,000<br>
also PV = EV - SV or 25,000 - 0 = 25,000<br>
<p>
<h3>DEDUCTIONS:</h3>
<p>
When looking at example C it's important to know that SV can equal zero. 
Actually, if you succeed in adhering to your schedule SV should always be close to zero. 
It's also interesting to note that in theory, before the project begins the SV equals zero because no spending or work has ocurred. 
At that point the EV and PV are both zero so 0 - 0 = 0. 
It is only over the course of the project that any value drifts away from zero. Zero is effectively your starting point. 
<p>
It's also worth noting another difference between the SV and CV calculations. 
Both measure project progress, and both do so in monetary units; neither of then in time-based units. 
This might make SV seem redundant to CV but it's not. 
Both formulas use EV and both subtract from it a single figure PV or AC. 
Structurally they are similar, and their values are related. 
At least that makes it all easier to memorize. 
<p>
Think of is as if EV is actual progress, PV is planned progress and AC is actual cost. 
The difference between EV and PV is actual progress vs planned progress, you either have or have not kept on schedule.
The difference between EV and AC is actual spending vs planned spending, you either have or have not kept on budget.
<p>
<h3>CONCLUSION:</h3>
This formula is simple logical and useful. It is easily understood and used far outside the PM sphere. 
Much like CV, it hardly requires comment. 
