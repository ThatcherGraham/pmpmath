<h3>Cost Variance</h3>	
<p>
Cost variance is a rock solid concept in the PMBOK. 
It's logical and can be used in essentially every formal and informal project, large or small by professional or amateur PMs. 
<p>
When you are mid-project there are two units by which you can measure your progress: time and money. 
Cost variance measures progress in dollars. It's that simple. It is the value of the work that has been accomplished thus far in a project. 
the formula is CV = EV - AC. EV is earned value and AC is actual cost. 
These two variables dont' have formulas. Actual cost is the sum of all direct project charges to date. It is the money you have already spent. 
<p>
Earned value is slightly less intuitive. EV is the estimated value of the completed work. 
That's a bit dicey though. On many projects the product or service is useless and unsaleable in an incomplete form. 
But that's not what they mean by the word "value." The formula for earned value is EV = percent complete x BAC. 
Sometimes this is given as BCWP - ACWP; Budgeted Cost of work performed minus Actual cost of work performed. 
You can see that here "value" is not the laypersons definition, here the meaning is more like planned cost. 
<p>
So the formula CV = EV - AC equates to planned spending minus actual spending. 
The formula produces a measure of the difference between the plan and reality. 
If you have spent more than was planned that formula will obviously produce a negative number. 
<p>
Ex. A<br>
EV = 25,000 and AC = 45,000<br>
So CV = 25,000 - 45,000 = -20,000
<p>
Ex. B<br>
EV = 45,000 and AC = 25,000<br>
So CV= 45,000 - 25,000 = 20,000
<p>
Ex. C<br>
EV = 25,000 and AC = 25,000<br>
So CV= 25,000 - 25,000 = 0<br>
<p>
Much like other simple project management formulas you can produce any of these figures from the other two. 
<p>
Ex. D<br>
EV = 25,000 and AC = 25,000<br>
So CV = EV - AC or 25,000 - 25,000 = 0<br>
and EV = AC + CV or 25,000 + 0 = 25,000<br>
also AC = EV - CV or 25,000 - 0 = 25,000<br>
<p>
<h3>DEDUCTIONS:</h3>
<p>
When looking at example C it's important to know that CV can equal zero. 
Actually, if you succeed in adhering to your schedule CV should always be close to zero. 
It's also interesting to note that, in theory before the project begins the CV equals zero because no spending or work has ocurred. 
At that point the EV and AC are both zero so 0 - 0 = 0. 
It is only over the course of the project that you drift away from zero. Zero is effectively your starting point. 
<p>
<h3>CONCLUSION:</h3>
Simple logical and useful. This an easily understood formula that is used far outside the PM sphere. It hardly requires comment. 
You need to understand what value means in this context. The rest you could learn running a cash register. 
